package com.evidu.bank.model;

public class CandidatesVO {
	private int candidate_id;
	private String candidate_name;
	private String candidate_position;
	private String candidate_department;
	private String candidate_image;
	private int election_id;
	private String candidate_telephone;
	
	
	private String candidate_email;
	public int getCandidate_id() {
		return candidate_id;
	}
	public void setCandidate_id(int candidate_id) {
		this.candidate_id = candidate_id;
	}
	public String getCandidate_name() {
		return candidate_name;
	}
	public void setCandidate_name(String candidate_name) {
		this.candidate_name = candidate_name;
	}
	public String getCandidate_position() {
		return candidate_position;
	}
	public void setCandidate_position(String candidate_position) {
		this.candidate_position = candidate_position;
	}
	public String getCandidate_department() {
		return candidate_department;
	}
	public void setCandidate_department(String candidate_department) {
		this.candidate_department = candidate_department;
	}
	public String getCandidate_image() {
		return candidate_image;
	}
	public void setCandidate_image(String candidate_image) {
		this.candidate_image = candidate_image;
	}
	public int getElection_id() {
		return election_id;
	}
	public void setElection_id(int election_id) {
		this.election_id = election_id;
	}
	public String getCandidate_telephone() {
		return candidate_telephone;
	}
	public void setCandidate_telephone(String candidate_telephone) {
		this.candidate_telephone = candidate_telephone;
	}
	public String getCandidate_email() {
		return candidate_email;
	}
	public void setCandidate_email(String candidate_email) {
		this.candidate_email = candidate_email;
	}
	
	
	
	
}
