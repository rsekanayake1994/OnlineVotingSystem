package com.evidu.bank.entitiy;

public class UserPages {
	private String autoId;
	private String pageName;
	private String pageId;
	private String activeSt;
	private String userRole;
	private String spanTitle;
	private String iclass;
	
	public String getAutoId() {
		return autoId;
	}
	public void setAutoId(String autoId) {
		this.autoId = autoId;
	}
	public String getPageName() {
		return pageName;
	}
	public void setPageName(String pageName) {
		this.pageName = pageName;
	}
	public String getPageId() {
		return pageId;
	}
	public void setPageId(String pageId) {
		this.pageId = pageId;
	}
	public String getActiveSt() {
		return activeSt;
	}
	public void setActiveSt(String activeSt) {
		this.activeSt = activeSt;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	public String getSpanTitle() {
		return spanTitle;
	}
	public void setSpanTitle(String spanTitle) {
		this.spanTitle = spanTitle;
	}
	public String getIclass() {
		return iclass;
	}
	public void setIclass(String iclass) {
		this.iclass = iclass;
	}
	
	
}
